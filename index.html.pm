◊section{Plán}

Účelem těchto stránek je poskytnout zdroj poznámek, ze kterého bude možné se pohodlně a kvalitně naučit na zkoušky. Má vize míří podobným směrem, kde jsou biosobory --- vytvoření jakéhosi ◊em{knowledge hubu} specializovaného na bioinformatické předměty --- avšak s několika rozdíly:
◊ls{
    - co nebude
        - žádné otravné pseudověty a gramatické chyby (případné chyby prosím hlašte)
        - žádné hloupé obarvování každé druhé věty
        - žádné špatně naformátované ◊code{.docx} soubory s nečitelnými obrázky
        - žádné čtení tří různých dokumentů o stejném tématu, aby vám nic neuniklo
    - co bude
        - webová, textová i pdf verze, aby každý mohl číst, jak chce
        - zápisky zkompilované z několika zdrojů, s doplněnými slidy (když jsou relevantní) a s doplněnými informacemi z internetu (kde jsou potřeba)
        - v budoucnu i jednoduchý způsob, jak reportnout gramatickou či faktickou chybu
}

Stránky jsou ještě ve svých raných začátcích, první zápisky jsou však již dostupné a na exportu do pdf se pracuje. Zároveň se snažím přizpůsobit stránku mobilům a menším počítačům --- to, co vidíte, je zatím velice hrubá verze.

◊section{Jak pomoct}

Stránky v blízké době zveřejním na GitHubu, takže se budete moci zapojit jak do psaní zápisů, tak do případného opravování překlepů a jiných chyb. V plánu je i zapojení jiných způsobů interakce, ty jsou však zatím ještě daleko. Pokud nyní najdete nějakou chybu, budete mít nějakou připomínku nebo jakýkoli nápad (pokrývající vše od designu přes formu psaní až po obsah), neváhejte a ◊strong{napište mi}.

