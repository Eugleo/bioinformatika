    - tři testy se píší v průběhu semestru, jsou dobrovolné, za bonusové body, které se počítají u zkoušky
        - 20 otázek, otázky na posledních pár přednášek
        - nezachrání čtyřku, ale jinak mohou zlepšit až o stupeň
        - zkouška má stejný formát
    - na cvikách řešení problémů v Moodlu, odevzdání se nekontroluje
        - otázky navazující na některé z úkolů se relativně často vyskytují v bonusových testech
