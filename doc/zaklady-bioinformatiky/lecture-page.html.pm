◊section{Informace o zkoušce}
◊ls{
    - zkouška se skládá ze dvou částí, celkově je za 50 bodů: písemná část (80%) a paper (20%), plus je možno získat bonusové body (maximálně 14)
    - většina informací (úkoly, cvičný test, témata na paper, termíny cvičení) bude na Moodlu
    - písmená část (40 bodů)
        - dlouhý test, zkušební bude na Moodlu
        - otestována budou dosti podrobně všechna témata
        - doporočeno vzít si s sebou pití a jídlo
    - paper (10 bodů + 3 bonusové)
        - zpracovat téma jednoho ze zadaných článků (použít ale i jiné zdroje)
        - uvést i osobní komentář: jaký to má přínos, jak to funguje, co si o tom myslím (viz News and views v časopise Nature)
        - nejlepší papery budou v zápočtovém týdnu prezentovány
    - domácí úkoly (dva po 3 bodech)
        - nepovinné (narozdíl od úkolů ve cvičení, viz níže)
        - lze je plnit samostastně nebo ve skupinách
        - jsou za bonusové body, které se počítají u zkoušky
    - rozšíření wikipedie (5 bodů)
        - rozpracovat téma týkající se bioinformatiky (nejprve konzultovat s Mariánem)
        - opět bonusové body ke zkoušce
}

